    private void configurarEventos() {
    txtBuscarMotivo.getDocument().addDocumentListener(new DocumentListener() {
        public void insertUpdate(DocumentEvent e) { buscarPorMotivo(); }
        public void removeUpdate(DocumentEvent e) { buscarPorMotivo(); }
        public void changedUpdate(DocumentEvent e) { buscarPorMotivo(); }
    });

    txtBuscarId.addActionListener(e -> buscarPorId(txtBuscarId.getText().trim()));
}
    
    private void buscarPorMotivo() {
    String motivo = txtBuscarMotivo.getText().trim();
    if (motivo.isEmpty()) {
        listaModel.clear();
        return;
    }

    new Thread(() -> {
        try {
            String encoded = URLEncoder.encode(motivo, "UTF-8");
            URL url = new URL("http://localhost:8080/flags/busca?=" + encoded);
            HttpURLConnection con = (HttpURLConnection) url.openConnection();
            con.setRequestMethod("GET");

            BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream()));
            String response = in.lines().collect(Collectors.joining());
            in.close();

            FlagResponseDTO[] flags = mapper.readValue(response, FlagResponseDTO[].class);

            SwingUtilities.invokeLater(() -> {
                listaModel.clear();
                for (FlagResponseDTO flag : flags) {
                    listaModel.addElement(flag.getId() + " - " + flag.getMotivo());
                }
            });

        } catch (Exception e) {
            e.printStackTrace();
        }
    }).start();
}
    
    private void buscarPorId(String idStr) {
    try {
        Long id = Long.parseLong(idStr);

        new Thread(() -> {
            try {
                URL url = new URL("http://localhost:8080/flags/buscaid?id=" + id);
                HttpURLConnection con = (HttpURLConnection) url.openConnection();
                con.setRequestMethod("GET");

                BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream()));
                String response = in.lines().collect(Collectors.joining());
                in.close();

                FlagResponseDTO[] flags = mapper.readValue(response, FlagResponseDTO[].class);

                SwingUtilities.invokeLater(() -> {
                    listaModel.clear();
                    for (FlagResponseDTO flag : flags) {
                        listaModel.addElement(flag.getId() + " - " + flag.getMotivo());
                    }
                });

            } catch (Exception e) {
                e.printStackTrace();
            }
        }).start();

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID inv√°lido.");
    }
}


